VALID_PROMPT_TYPES = ["VERILOG_ONLY", "VERILOG_AND_VERILATOR", "VERILOG_AND_VITIS_CPP"]
REQUIRED_SAMPLE_KEYS  = ["code", "full_code", "index"] 


def load_json(file_name):
    with open(file_name, 'r') as json_file:
        return json.load(json_file)
    
def load_jsonl(file_name):
    data = []
    with jsonlines.open(file_name) as reader:
        for obj in reader:
            data.append(obj)
    return data

def generate_prompt(sample, sample_type):
    assert sample_type in VALID_PROMPT_TYPES, print("ERROR:", sample_type, "not in", VALID_PROMPT_TYPES)
    if sample_type  == "VERILOG_ONLY":
        return VERILOG_ONLY_PROMPT + sample['full_code']
    elif sample_type == "VERILOG_AND_VERILATOR":
        return VERILOG_AND_VERILATOR_CPP_PROMPT_PART_1_OF_2 + sample['cpp_code'] + VERILOG_AND_VERILATOR_CPP_PROMPT_PART_2_OF_2 + sample['full_code']
    else:
        return VERILOG_AND_VITIS_CPP_PROMPT_PART_1_OF_3 + sample['full_code'] + VERILOG_AND_VITIS_CPP_PROMPT_PART_2_OF_3 + sample['cpp_code'] + VERILOG_AND_VITIS_CPP_PROMPT_PART_3_OF_3

def send_prompt_to_vllm(input_text, llm_checkpoint_dir, max_new_tokens, max_input_length=40*1024):
    content = ""
    reasoning_content = ""
    try:
        client = OpenAI(api_key=VLLM_API_KEY, base_url=VLLM_API_BASE)
        chat_completion = client.chat.completions.create(
            messages=[{
                "role": "system",
                "content": "You are a helpful assistant."
            }, {
                "role": "user",
                "content": input_text+"\n"
            }],
            model=llm_checkpoint_dir,
        )
        # extract LLM response
        if len(chat_completion.to_dict()['choices'][0]['message']['content']) > 0:
            content = chat_completion.to_dict()['choices'][0]['message']['content']
        # extract LLM reasoning
        if len(chat_completion.to_dict()['choices'][0]['message']['reasoning_content']) > 0:
            reasoning_content = chat_completion.to_dict()['choices'][0]['message']['reasoning_content']
    except Exception as e:
        print(e)
    return content, reasoning_content

def process_and_save_batch(batch, batch_num, save_path, llm_checkpoint_dir, max_new_tokens, prompt_type):
    """Process a batch of modules one by one, and save the results evey batch_num samples in JSONL format."""
    output_data = []
    for idx, result in tqdm(enumerate(batch), total=len(batch), desc=f"Processing batch {batch_num}", leave=False):
        for k in REQUIRED_SAMPLE_KEYS:
            assert k in result.keys(), print("ERROR: sample does not contain required key:", k)
        input_sample_idx = result['index']
        verilog_code = result['full_code']
        verilog_code_woio = result['code']
        ioheader = verilog_code.replace(verilog_code_woio, "")
        input_text = generate_prompt(sample=result, sample_type=prompt_type)
        content, reasoning_content = send_prompt_to_vllm(input_text, llm_checkpoint_dir=llm_checkpoint_dir, max_new_tokens=max_new_tokens)
        output_data.append({
            'index': input_sample_idx,
            'verilog_code': verilog_code_woio,
            'full_code': verilog_code,
            'ioheader': ioheader,
            'llm_response': content,
            'llm_reasoning': reasoning_content
        })
    with jsonlines.open(save_path, 'a') as f:
        f.write_all(output_data)


def process_file(file_path, save_path, llm_checkpoint_dir, start_idx, end_idx, prompt_type, batch_size=1, max_new_tokens=8192):
    """Process the JSON file in batches and save results."""
    # calculate batch runtime metrics, convert end_idx from inclusive to exclusive
    inputs = load_jsonl(file_path)
    end_idx = min(end_idx+1, len(inputs))
    total_batches = (end_idx - start_idx) // batch_size
    # calculate where to resume labeling or start from idx 0
    finished_num_samples = 0
    if os.path.isfile(save_path):
        finished_num_samples = len(load_jsonl(save_path))
    # loop and process batches
    for i in tqdm(range(start_idx, end_idx, batch_size), total=total_batches, desc="Processing batches"):
        batch = inputs[i:min(end_idx,i+batch_size)]
        batch_num = i // batch_size
        if i+batch_size < start_idx+finished_num_samples:
            continue
        elif i < start_idx+finished_num_samples:
            batch = inputs[start_idx+finished_num_samples:min(end_idx,i+batch_size)]
        process_and_save_batch(batch=batch, batch_num=batch_num, save_path=save_path,
                                llm_checkpoint_dir=llm_checkpoint_dir, max_new_tokens=max_new_tokens, prompt_type=prompt_type)


def parse_args():
    parser = argparse.ArgumentParser(description="Process Verilog files using a range of entries.")
    parser.add_argument('--start_idx', type=int, default=0, help='Starting index for processing.')
    parser.add_argument('--end_idx', type=int, default=2, help='Ending index for processing, inclusive.')
    parser.add_argument('--src_path', type=str, default="", help='Absolute path of the file that contains source dataset to be labeled.')
    parser.add_argument('--save_path', type=str, default="", help='Absolute path of the file that saves all inference results.')
    parser.add_argument('--prompt_type', type=str, default="", help="One of [[\"VERILOG_ONLY\", \"VERILOG_AND_VERILATOR\", \"VERILOG_AND_VITIS_CPP\"]]")
    return parser.parse_args()


