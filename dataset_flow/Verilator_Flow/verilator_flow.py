import os
import subprocess
import json

def run_verilator_on_verilog(verilog_file, output_folder):
    """
    Runs Verilator on the given Verilog file and generates the corresponding C++ files.
    """
    # Get the name of the Verilog file without extension
    base_name = os.path.splitext(os.path.basename(verilog_file))[0]

    # Define the output folder for Verilator (where C++ files will be stored)
    verilator_output_dir = os.path.join(output_folder, base_name + "_verilator_output")

    # Create the output directory if it doesn't exist
    if not os.path.exists(verilator_output_dir):
        os.makedirs(verilator_output_dir)

    # Run Verilator without --output-dir option
    command = [
        'verilator',
        '--cc', verilog_file,   # Generate C++ code
        '--top', base_name,      # Specify the top module name, typically the same as the file base name
    ]
    
    try:
        subprocess.run(command, check=True, cwd=verilator_output_dir)  # Run in the output directory
        print(f"Verilator run successful for {verilog_file}")
    except subprocess.CalledProcessError as e:
        print(f"Error running Verilator on {verilog_file}: {e}")
        return None
    
    return verilator_output_dir

def concatenate_cpp_files(verilator_output_dir, verilog_file):
    """
    Concatenates all generated C++ files in the verilator output directory
    and saves them with the same name as the Verilog file but with a .v extension.
    """
    # Get the name of the Verilog file without extension
    base_name = os.path.splitext(os.path.basename(verilog_file))[0]

    # Define the output file path
    concatenated_cpp_file = os.path.join(os.path.dirname(verilog_file), f"{base_name}.cpp")

    # Open the concatenated file in write mode
    with open(concatenated_cpp_file, 'w') as output_file:
        # Loop through all C++ files generated by Verilator
        for root, dirs, files in os.walk(verilator_output_dir):
            for file in files:
                if file.endswith('.cpp'):
                    cpp_file_path = os.path.join(root, file)
                    print(f"Adding {cpp_file_path} to the concatenated file.")
                    
                    with open(cpp_file_path, 'r') as cpp_file:
                        output_file.write(cpp_file.read())
    
    print(f"Concatenated C++ files saved as: {concatenated_cpp_file}")
    return concatenated_cpp_file

def process_verilog_files_in_folder(folder_path, jsonl_file):
    """
    Processes all Verilog files in the given folder and its subfolders,
    runs Verilator, concatenates the generated C++ files, and writes the results to a JSONL file.
    """
    with open(jsonl_file, 'a') as jsonl:
        # Walk through the folder and its subfolders
        for root, dirs, files in os.walk(folder_path):
            for file in files:
                if file.endswith('.v'):  # Look for Verilog files
                    verilog_file = os.path.join(root, file)
                    print(f"Processing Verilog file: {verilog_file}")
                    
                    # Read the Verilog code
                    with open(verilog_file, 'r', encoding='latin-1') as f:
                        verilog_code = f.read()
                    
                    # Run Verilator and generate C++ files
                    verilator_output_dir = run_verilator_on_verilog(verilog_file, root)
                    cpp_code = ""

                    if verilator_output_dir:
                        # Concatenate all C++ files into one string
                        cpp_file = concatenate_cpp_files(verilator_output_dir, file)
                        with open(cpp_file, 'r', encoding='utf-8') as f:
                            cpp_code = f.read()


                    # Prepare the JSON object
                    json_object = {
                        "verilog_code": verilog_code,
                        "cpp_code": cpp_code # Empty string if no C++ code generated
                    }
                    
                    # Write the JSON object as a line in the JSONL file
                    jsonl.write(json.dumps(json_object) + "\n")
                    print(f"Processed and saved entry for {verilog_file}")

# Example usage
if __name__ == "__main__":
    folder_path = ""  # Replace with your folder path
    jsonl_file = "verilog_cpp_output.jsonl"  # The output JSONL file
    process_verilog_files_in_folder(folder_path, jsonl_file)